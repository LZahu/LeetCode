class Solution {
    public TreeNode lowestCommonAncestor(TreeNode root, TreeNode p, TreeNode q) {
        Map<Integer, TreeNode> parents = new HashMap<>();
        dfs(root, parents);

        Set<Integer> visited = new HashSet<>();
        // ä»på¾€ä¸Šæ‰¾ï¼Œä¿å­˜æ‰€æœ‰çš„çˆ¶èŠ‚ç‚¹ï¼ŒåŒ…æ‹¬è‡ªå·±
        while (p != null){
            visited.add(p.val);
            p = parents.get(p.val);
        }
        // ä»qå¾€ä¸Šæ‰¾ï¼Œé‡åˆçš„èŠ‚ç‚¹å³ä¸ºå…¬å…±ç¥–å…ˆ
        while (q != null){
            if (visited.contains(q.val)){
                return q;
            }
            q = parents.get(q.val);
        }

        return null;
    }

    // ä¿å­˜æ¯ä¸€ä¸ªèŠ‚ç‚¹å’Œå®ƒçš„çˆ¶èŠ‚ç‚¹çš„å…³ç³»
    public void dfs(TreeNode node, Map<Integer, TreeNode> parents){
        if (node == null){
            return;
        }

        if (node.left != null){
            parents.put(node.left.val, node);
            dfs(node.left, parents);
        }
        if (node.right != null){
            parents.put(node.right.val, node);
            dfs(node.right, parents);
        }
    }
}
